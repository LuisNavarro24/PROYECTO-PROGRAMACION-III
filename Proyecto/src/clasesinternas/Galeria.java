    public Icon setIcono(String url, JLabel imagen){
        ImageIcon icon = new ImageIcon(getClass().getResource(url));
        ImageIcon icono = new ImageIcon(icon.getImage().getScaledInstance(450, 300, Image.SCALE_DEFAULT));
        return icono;
    }
    
