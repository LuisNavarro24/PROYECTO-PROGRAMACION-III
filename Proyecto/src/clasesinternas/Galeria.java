    private void jButtonSiguienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSiguienteActionPerformed
        if(band==fotos.length-1){
            band = 0;
            this.jLabelTitulo.setText("~[Hotel]~");
            this.jLabelImagenes.setIcon(setIcono("/imagenesgaleria/"+fotos[band], jLabelImagenes));
        }else{
            band++;
            if(band == 3)
                this.jLabelTitulo.setText("~[Habitación Individual]~");
            if(band == 4)
                this.jLabelTitulo.setText("~[Habitación Doble]~");
            if(band == 5)
                this.jLabelTitulo.setText("~[Habitación Familiar]~");
            if(band == 6)
                this.jLabelTitulo.setText("~[Cafeteria]~");
            if(band == 7)
                this.jLabelTitulo.setText("~[Gimnasio]~");
            if(band == 8)
                this.jLabelTitulo.setText("~[Piscina]~");
            if(band == 9)
                this.jLabelTitulo.setText("~[Spa]~");
            this.jLabelImagenes.setIcon(setIcono("/imagenesgaleria/"+fotos[band], jLabelImagenes));
        }
    }//GEN-LAST:event_jButtonSiguienteActionPerformed

    private void jButtonAtrasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAtrasActionPerformed
        if(band==0){
            band = 9;
            this.jLabelTitulo.setText("~[Spa]~");
            this.jLabelImagenes.setIcon(setIcono("/imagenesgaleria/"+fotos[band], jLabelImagenes));
        }else{
            band--;
            if(band == 1)
                this.jLabelTitulo.setText("~[Hotel]~");
            if(band == 3)
                this.jLabelTitulo.setText("~[Habitación Individual]~");
            if(band == 4)
                this.jLabelTitulo.setText("~[Habitación Doble]~");
            if(band == 5)
                this.jLabelTitulo.setText("~[Habitación Familiar]~");
            if(band == 6)
                this.jLabelTitulo.setText("~[Cafeteria]~");
            if(band == 7)
                this.jLabelTitulo.setText("~[Gimnasio]~");
            if(band == 8)
                this.jLabelTitulo.setText("~[Piscina]~");
            this.jLabelImagenes.setIcon(setIcono("/imagenesgaleria/"+fotos[band], jLabelImagenes));
        }
    }//GEN-LAST:event_jButtonAtrasActionPerformed
    
